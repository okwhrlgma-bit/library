def search_kdc(query: str, *, limit: int = 10) -> list[tuple[str, str]]:
    """검색어로 KDC 후보 찾기.

    예: search_kdc("소설") → [("813", "한국소설"), ("823", "중국소설"), ...]
    """
    if not query:
        return []
    query = query.strip().lower()
    matches: list[tuple[str, str]] = []

    for main_code, main in KDC_6_TREE.items():
        if query in main["name"]:
            matches.append((main_code, main["name"]))
        for sub_code, sub_name in main.get("children", {}).items():
            if query in sub_name.lower() or query in sub_name:
                matches.append((sub_code, sub_name))

    return matches[:limit]


def get_subtree(main_code: str) -> dict[str, str]:
    """주류 코드(예: '800')의 강목 dict."""
    main = KDC_6_TREE.get(main_code)
    if not main:
        return {}
    return main.get("children", {})


def get_main_classes() -> list[tuple[str, str]]:
    """주류 0~9 (UI 첫 화면용)."""
    return [(code, info["name"]) for code, info in KDC_6_TREE.items()]
